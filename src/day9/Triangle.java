package day9;

public class Triangle extends Figure {
    private double a;
    private double b;
    private double c;

    public Triangle(double a, double b, double c, String color) {
        super(color);
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    @Override
    public double area() {
        return Math.sqrt((perimeter() / 2) * (perimeter() / 2 - getA()) * (perimeter() / 2 - getB()) * (perimeter() / 2 - getC()));
    }

    @Override
    public double perimeter() {
        return a + b + c;
    }
}
